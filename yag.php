<p style="text-indent: 1.5em">Когда-то на острове, на вершине холма, стояла церковь, пройти к которой можно было по деревянному мосту. От берега к церкви вела липовая аллея. Старые липы до сих пор выделяются среди других деревьев. У самой церкви до 1950-х годов было кладбище. Местные жители рассказывают, что и теперь там находят могильные камни с именами крепостных, почивших еще в 19 веке. Церковь на острове была разрушена во времена СССР. А вот остатки моста в виде торчащих из воды деревянных столбов сохранились и по сей день.</p>
