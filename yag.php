<p style="text-indent: 1.5em">Это озеро является главной достопримечательностью села Яганово.  Водоем небольшой, своей формой он напоминает кольцо. А в центре озера располагается круглый островок. Местные жители говорят, что местами глубина озера достигает 13-15 метров.
</br></br>У этой сельской достопримечательности есть своя легенда. Она гласит, что во время монголо-татарского ига здесь проживал хан Яган, приказавший крестьянам выкопать озеро. Те, конечно, повиновались. Копали и складывали землю посредине ямы – так и появился  в центре озера остров, о котором говорилось выше.  Когда крестьяне выполнили свою работу, хан решил попробовать воду на вкус. Она ему не понравилась: соленая. Реакция хана стоила ему жизни: разозлились на него мужики, утопили в озере. Вот поэтому оно и называется Ягановским.
</p>
